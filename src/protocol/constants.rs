/// 复权类型
///
/// v0.4.1: 服务端 CMD_SECURITY_BARS 返回原始未复权数据 (fq 字段为协议保留位, 不控制复权)。
/// 复权现在由客户端侧 `adjuster` 模块根据除权信息 (get_xdxr_info) 计算完成。
/// `fq` 参数传递到 get_security_bars() 中触发客户端调整: 0=原始, 1=前复权, 2=后复权。
pub mod fq_type {
    pub const NONE: u8 = 0;  // 未复权 — 交易所原始价格
    pub const QFQ: u8 = 1;   // 前复权 — 客户端侧计算 (历史价格后向调整, 默认)
    pub const HFQ: u8 = 2;   // 后复权 — 客户端侧计算 (近期价格前向调整)
}

